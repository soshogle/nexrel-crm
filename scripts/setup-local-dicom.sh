#!/bin/bash

# Complete Local DICOM Setup Script
# Sets up everything needed for local DICOM testing

set -e

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m'

echo ""
echo -e "${BLUE}🚀 Setting Up Local DICOM Testing Environment${NC}"
echo ""

# Step 1: Check Docker
echo -e "${GREEN}Step 1: Checking Docker...${NC}"
if ! command -v docker &> /dev/null; then
    echo -e "${RED}❌ Docker not found!${NC}"
    echo "   Install Docker: https://docs.docker.com/get-docker/"
    exit 1
fi
echo -e "${GREEN}✅ Docker installed${NC}"

# Step 2: Add environment variables
echo ""
echo -e "${GREEN}Step 2: Setting up environment variables...${NC}"
ENV_FILE=".env.local"

if [ ! -f "$ENV_FILE" ]; then
    echo -e "${YELLOW}⚠️  .env.local not found. Creating...${NC}"
    touch "$ENV_FILE"
fi

# Check if Orthanc vars already exist
if grep -q "ORTHANC_BASE_URL" "$ENV_FILE"; then
    echo -e "${GREEN}✅ Orthanc variables already exist${NC}"
else
    echo "" >> "$ENV_FILE"
    echo "# Orthanc DICOM Server Configuration" >> "$ENV_FILE"
    echo "ORTHANC_BASE_URL=http://localhost:8042" >> "$ENV_FILE"
    echo "ORTHANC_USERNAME=orthanc" >> "$ENV_FILE"
    echo "ORTHANC_PASSWORD=orthanc" >> "$ENV_FILE"
    echo "" >> "$ENV_FILE"
    echo "# DICOM Network Configuration" >> "$ENV_FILE"
    echo "DICOM_AE_TITLE=NEXREL-CRM" >> "$ENV_FILE"
    echo "ORTHANC_HOST=localhost" >> "$ENV_FILE"
    echo "ORTHANC_PORT=4242" >> "$ENV_FILE"
    echo "" >> "$ENV_FILE"
    echo "# Webhook Secret" >> "$ENV_FILE"
    echo "DICOM_WEBHOOK_SECRET=local-test-secret-change-in-production" >> "$ENV_FILE"
    echo "" >> "$ENV_FILE"
    echo "# Next.js API URL" >> "$ENV_FILE"
    echo "NEXT_PUBLIC_API_URL=http://localhost:3000" >> "$ENV_FILE"
    echo -e "${GREEN}✅ Added Orthanc variables to .env.local${NC}"
fi

# Step 3: Start Orthanc
echo ""
echo -e "${GREEN}Step 3: Starting Orthanc...${NC}"
if curl -sf http://localhost:8042/system > /dev/null 2>&1; then
    echo -e "${GREEN}✅ Orthanc is already running${NC}"
else
    echo "Starting Orthanc..."
    ./scripts/start-local-orthanc.sh
    sleep 5
    
    if curl -sf http://localhost:8042/system > /dev/null 2>&1; then
        echo -e "${GREEN}✅ Orthanc started successfully${NC}"
    else
        echo -e "${RED}❌ Failed to start Orthanc${NC}"
        echo "   Check logs: docker logs nexrel-orthanc"
        exit 1
    fi
fi

# Step 4: Download sample DICOM files
echo ""
echo -e "${GREEN}Step 4: Downloading sample DICOM files...${NC}"
if [ -d "test-data/dicom-samples" ] && [ -n "$(ls -A test-data/dicom-samples/*.dcm 2>/dev/null)" ]; then
    echo -e "${GREEN}✅ Sample DICOM files already exist${NC}"
else
    ./scripts/download-sample-dicom.sh
fi

# Step 5: Summary
echo ""
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━